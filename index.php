    <script>
    // Tab switching logic for UU ORMAS section
    document.querySelectorAll('.uu-tab-btn').forEach(function(btn) {
      btn.addEventListener('click', function() {
        // Remove active from all buttons
        document.querySelectorAll('.uu-tab-btn').forEach(function(b) {
          b.classList.remove('active');
        });
        // Hide all tab panes
        document.querySelectorAll('.uu-tab-pane').forEach(function(pane) {
          pane.style.display = 'none';
          pane.classList.remove('active');
        });
        // Activate clicked button and show corresponding pane
        btn.classList.add('active');
        var target = btn.getAttribute('data-target');
        var pane = document.getElementById(target);
        if (pane) {
          pane.style.display = '';
          pane.classList.add('active');
        }
      });
    });
    </script>

